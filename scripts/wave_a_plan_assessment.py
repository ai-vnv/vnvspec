"""Wave A self-hosting: assess build-plan phase goals as requirements.

Treats each of the 16 build-plan phase goals as a synthetic
Requirement and runs the INCOSE GtWR quality checker against it.
Outputs a Markdown report to docs/development/plan-self-assessment.md.
"""

