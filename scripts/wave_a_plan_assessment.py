def _build_requirements() -> list[Requirement]:
    """Create synthetic Requirement objects from phase goals."""
    reqs: list[Requirement] = []
    for req_id, statement in _PHASE_GOALS:
        reqs.append(
            Requirement(
                id=req_id,
                statement=statement,
                rationale=("Build-plan milestone for vnvspec development."),
                verification_method="inspection",
                acceptance_criteria=["Phase deliverable is complete and passes CI."],
            )
        )
    return reqs


def _format_violations(
    violations: list[RuleViolation],
) -> str:
    """Format a list of violations as a markdown bullet list."""
    if not violations:
        return "  - *No violations*\n"
    lines: list[str] = []
    for v in violations:
        lines.append(f"  - **{v.rule} {v.name}** ({v.severity}): {v.message}")
    return "\n".join(lines) + "\n"


def _generate_report(
    reqs: list[Requirement],
) -> str:
    """Run check_quality on each requirement, return markdown."""
    lines: list[str] = [
        "# Wave A Plan Self-Assessment",
        "",
        "This report was auto-generated by `scripts/wave_a_plan_assessment.py`.",
        "It treats each build-plan phase goal as a synthetic Requirement",
        "and runs the INCOSE GtWR quality checker against it.",
        "",
        "## Results",
        "",
    ]
    total_violations = 0
    phases_with_violations = 0

    for req in reqs:
        violations = req.check_quality()
        total_violations += len(violations)
        if violations:
            phases_with_violations += 1

        lines.append(f"### {req.id}")
        lines.append("")
        lines.append(f"**Statement:** {req.statement}")
        lines.append("")
        lines.append(f"**Violations ({len(violations)}):**")
        lines.append("")
        lines.append(_format_violations(violations))

    lines.append("## Summary")
    lines.append("")
    lines.append(f"- **Total phases:** {len(reqs)}")
    lines.append(f"- **Phases with violations:** {phases_with_violations}")
    lines.append(f"- **Total violations:** {total_violations}")
    lines.append("")
    lines.append("Most violations stem from the fact that build-plan goals are")
    lines.append("informal milestones, not engineering requirements. They lack")
    lines.append("'shall' language, clear subjects, and are often compound.")
    lines.append("This is expected and demonstrates that the GtWR checker")
    lines.append("correctly flags non-requirement prose.")
    lines.append("")
    return "\n".join(lines)


def main() -> None:
    """Entry point for the Wave A self-hosting assessment."""
    reqs = _build_requirements()
    report = _generate_report(reqs)

    repo_root = Path(__file__).resolve().parent.parent
    out_path = repo_root / "docs" / "development" / "plan-self-assessment.md"
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(report, encoding="utf-8")

    print(f"Report written to {out_path}")
    print()
    print(report)


if __name__ == "__main__":
    main()
